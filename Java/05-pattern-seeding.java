import java.util.Scanner;

public class Solution {
    public static void seeding(int n) {
        // Write your code here
        for(int i = 0; i<n; i++){
            for(int j = n; j>i; j--){
                System.out.print("*" + " ");
            }
            System.out.println();
        }
    }

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int n = scanner.nextInt();
        seeding(n);
        scanner.close();
    }
}
